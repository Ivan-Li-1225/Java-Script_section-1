        System.out.println("v1" + " "  + "(10進位)" + ":" + " " + v1);

        // 2進位

        int v2 = 0b0101;
        System.out.println("v2" + " " + "(2進位)" + ":" + " " + v2);

        // 8進位

        int v3 = 0307;
        System.out.println("v3" + " " + "(8進位)" + ":" + " " + v3);

        // 16進位

        int v4 = 0xF010;
        System.out.println("v4" + " " + "(16進位)" + ":" + " " + v4);


