public class Ch2_5 {
    public static void main(String[] args) {

        int hp = 2;
        System.out.println("hp" + " " + "=" + " " + hp);
        System.out.println("========");

        hp = hp + 5;
        System.out.println("hp" + " " + "=" + " " + hp);
        System.out.println("========");

        hp = hp - 2;
        System.out.println("hp" + " " + "=" + " " + hp);
        System.out.println("========");

        hp += 6;
        System.out.println("hp" + " " + "=" + " " + hp);
        System.out.println("========");

        hp -= 3;
        System.out.println("hp" + " " + "=" + " " + hp);
        System.out.println("========");

        hp *= 2;
        System.out.println("hp" + " " + "=" + " " + hp);
        System.out.println("========");

        hp /= 2;
        System.out.println("hp" + " " + "=" + " " + hp);
        System.out.println("========");

        hp %= 2;
        System.out.println("hp" + " " + "=" + " " + hp);
        System.out.println("========");

    }
}
